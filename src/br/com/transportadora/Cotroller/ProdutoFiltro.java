package br.com.transportadora.Cotroller;

public class ProdutoFiltro {
	
	private double peso;
	private double valor;
	
	public void setPeso(double peso) {
		this.peso = peso;
	}
	public double getPeso() {
		return peso;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public double getValor() {
		return valor;
	}
	

}
